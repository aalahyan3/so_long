#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	return_val;

	if (dst == src && dst != NULL && src != NULL)
		return (0);
	return_val = ft_strlen(src);
	if (dstsize == 0)
		return (return_val);
	i = 0;
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (return_val);
}
